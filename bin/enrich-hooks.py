#!/usr/bin/env python3
"""
Hook Documentation AI Enricher for Uncanny Automator.

Reads Markdown hook files generated by scan-hooks.php, finds {{PLACEHOLDER}}
patterns, calls an AI provider to fill them, and writes back in-place.

Follows the same architecture as generate_ai_content.py from automator-item-exporter.

Supported AI Providers:
    - Google Gemini (gemini)
    - Claude/Anthropic (claude)
    - OpenAI (openai)
    - OpenRouter (openrouter)

Usage:
    python3 bin/enrich-hooks.py --input hook-docs/docs
    python3 bin/enrich-hooks.py --input hook-docs/docs --force --workers 8

Requirements:
    pip install google-genai anthropic openai requests python-dotenv
"""

