#!/usr/bin/env php
<?php
/**
 * Shared integration map generator for Free, Pro, and addon plugins.
 *
 * Scans src/integrations/ (plural) and src/integration/ (singular) directories
 * for integration files and builds autoload_integrations_map.php.
 *
 * Handles both patterns:
 *   - add-*-integration.php (legacy Free/Pro pattern)
 *   - *-integration.php     (modern addon pattern)
 *
 * Subdirectories scanned: actions, helpers, tokens, triggers, closures, conditions, loop-filters
 *
 * Usage:
 *   php bin/generate-load-files.php --plugin-path /path/to/plugin
 *
 * Output:
 *   {plugin-path}/vendor/composer/autoload_integrations_map.php
 */

$options = getopt( '', array( 'plugin-path:' ) );

if ( empty( $options['plugin-path'] ) ) {
	fwrite( STDERR, "Usage: php generate-load-files.php --plugin-path /path/to/plugin\n" );
	exit( 1 );
}

$plugin_path = realpath( rtrim( $options['plugin-path'], DIRECTORY_SEPARATOR ) );

if ( false === $plugin_path || ! is_dir( $plugin_path ) ) {
	fwrite( STDERR, "ERROR: Plugin path does not exist: {$options['plugin-path']}\n" );
	exit( 1 );
}

$default_directories = array( 'actions', 'helpers', 'tokens', 'triggers', 'closures', 'conditions', 'loop-filters' );
$integrations_map    = array();

// Scan both src/integrations/ (plural) and src/integration/ (singular).
$scan_dirs = array(
	$plugin_path . '/src/integrations',
	$plugin_path . '/src/integration',
);

foreach ( $scan_dirs as $directory ) {
	if ( ! is_dir( $directory ) ) {
		continue;
	}

	// Check if this is a single-integration directory (src/integration/ with *-integration.php directly in it).
	$direct_integration_files = glob( $directory . '/*-integration.php' );
	if ( ! empty( $direct_integration_files ) ) {
		// Single-integration addon (e.g., src/integration/custom-user-fields-integration.php).
		$integration_name = basename( $directory );
		$integration_data = build_integration_data( $directory, $direct_integration_files[0], $plugin_path, $default_directories );
		if ( ! empty( $integration_data ) ) {
			$integrations_map[ $integration_name ] = $integration_data;
		}
		continue;
	}

	// Multi-integration directory (e.g., src/integrations/{name}/).
	$integration_dirs = glob( $directory . '/*', GLOB_ONLYDIR );
	foreach ( $integration_dirs as $integration_dir ) {
		$integration_name = basename( $integration_dir );

		if ( 'vendor' === $integration_name ) {
			continue;
		}

		// Find main integration file: add-*-integration.php or *-integration.php.
		$main_files = glob( $integration_dir . '/add-*-integration.php' );
		if ( empty( $main_files ) ) {
			$main_files = glob( $integration_dir . '/*-integration.php' );
		}

		$main_file = ! empty( $main_files ) ? $main_files[0] : null;

		$integration_data = build_integration_data( $integration_dir, $main_file, $plugin_path, $default_directories );
		if ( ! empty( $integration_data ) ) {
			$integrations_map[ $integration_name ] = $integration_data;
		}
	}
}

if ( empty( $integrations_map ) ) {
	fwrite( STDERR, "No integrations found in {$plugin_path}\n" );
	exit( 1 );
}

// Build the output file.
$map_content  = '<?php' . PHP_EOL;
$map_content .= '// Auto-generated by generate-load-files.php — do not edit.' . PHP_EOL;
$map_content .= '$vendorDir = dirname(dirname(__FILE__));' . PHP_EOL;
$map_content .= '$baseDir = dirname($vendorDir);' . PHP_EOL;
$map_content .= 'return array(' . PHP_EOL;

foreach ( $integrations_map as $name => $data ) {
	$map_content .= "\t'" . $name . "' => array(" . PHP_EOL;

	foreach ( $data as $key => $value ) {
		if ( 'main' === $key ) {
			$map_content .= "\t\t'main' => \$baseDir . '" . $value . "'," . PHP_EOL;
		} else {
			$map_content .= "\t\t'" . $key . "' => array(" . PHP_EOL;
			foreach ( $value as $file_rel ) {
				$map_content .= "\t\t\t\$baseDir . '" . $file_rel . "'," . PHP_EOL;
			}
			$map_content .= "\t\t)," . PHP_EOL;
		}
	}

	$map_content .= "\t)," . PHP_EOL;
}

$map_content .= ');';

$map_file_path = $plugin_path . '/vendor/composer/autoload_integrations_map.php';

if ( false === file_put_contents( $map_file_path, $map_content, LOCK_EX ) ) {
	fwrite( STDERR, "Failed to write integrations map file: {$map_file_path}\n" );
	exit( 1 );
}

$count = count( $integrations_map );
fwrite( STDOUT, "Generated autoload_integrations_map.php with {$count} integration(s)\n" );
fwrite( STDOUT, "Written to: {$map_file_path}\n" );

// ============================================================
// Helper functions
// ============================================================

/**
 * Build integration data array from a directory.
 *
 * @param string      $integration_dir     The integration directory.
 * @param string|null $main_file           The main integration file (or null).
 * @param string      $plugin_path         The plugin root path (for relative paths).
 * @param array       $default_directories Subdirectories to scan.
 *
 * @return array  Integration data with 'main', 'actions', 'triggers', etc.
 */
function build_integration_data( $integration_dir, $main_file, $plugin_path, $default_directories ) {

	$integration_data = array();

	// Main file.
	if ( null !== $main_file && is_file( $main_file ) ) {
		$relative                 = str_replace( $plugin_path, '', $main_file );
		$relative                 = str_replace( '\\', '/', $relative );
		$integration_data['main'] = $relative;
	}

	// Find files in standard subdirectories.
	foreach ( $default_directories as $sub_dir ) {
		$sub_path = $integration_dir . '/' . $sub_dir;
		if ( ! is_dir( $sub_path ) ) {
			continue;
		}
		$php_files = glob( $sub_path . '/*.php' );
		foreach ( $php_files as $php_file ) {
			if ( 'index.php' === basename( $php_file ) ) {
				continue;
			}
			$relative                       = str_replace( $plugin_path, '', $php_file );
			$relative                       = str_replace( '\\', '/', $relative );
			$integration_data[ $sub_dir ][] = $relative;
		}
	}

	return $integration_data;
}
