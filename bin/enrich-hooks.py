# ---------------------------------------------------------------------------
# Markdown parsing helpers
# ---------------------------------------------------------------------------

def parse_frontmatter(content: str) -> Tuple[Dict[str, str], str]:
    """Parse YAML frontmatter from markdown content."""
    meta = {}
    body = content

    if content.startswith('---'):
        parts = content.split('---', 2)
        if len(parts) >= 3:
            for line in parts[1].strip().split('\n'):
                if ':' in line:
                    key, _, val = line.partition(':')
                    meta[key.strip()] = val.strip().strip('"').strip("'")
            body = parts[2]

    return meta, body


def extract_context_from_md(content: str) -> Dict[str, str]:
    """Extract hook context from a markdown file for prompt building."""
    meta, body = parse_frontmatter(content)

    ctx = {
        'hook_id': meta.get('hook_id', ''),
        'hook_type': meta.get('hook_type', 'action'),
        'plugin': meta.get('plugin', ''),
        'integration': meta.get('integration', ''),
        'since': meta.get('since', ''),
        'dynamic': meta.get('dynamic', 'false'),
    }

    # Extract parameters section.
    params_match = re.search(r'## Parameters\n\n(.*?)(?=\n---|\n## )', body, re.DOTALL)
    ctx['parameters'] = params_match.group(1).strip() if params_match else ''

    # Extract source code section.
    source_match = re.search(r'## Source Code\n\n(.*?)(?=\n---|\n## |\Z)', body, re.DOTALL)
    ctx['source_code'] = source_match.group(1).strip() if source_match else ''

    # Extract existing description (from docblock, before the placeholder).
    # Look for text between the Since line and the first {{READABLE_DESCRIPTION}}.
    desc_match = re.search(r'\*\*Since:\*\*.*?\n\n(.*?)(?={{READABLE_DESCRIPTION}})', body, re.DOTALL)
    ctx['existing_description'] = desc_match.group(1).strip() if desc_match else ''

    # Extract @example blocks already in the file.
    examples = re.findall(r'```php\n(.*?)```', body, re.DOTALL)
    ctx['existing_examples'] = examples

    # Extract internal usage section.
    usage_match = re.search(r'## Internal Usage\n\n(.*?)(?=\n---|\n## |\Z)', body, re.DOTALL)
    ctx['internal_usage'] = usage_match.group(1).strip() if usage_match else ''

    # Extract return value section (for filters).
    return_match = re.search(r'## Return Value\n\n(.*?)(?=\n---|\n## )', body, re.DOTALL)
    ctx['return_section'] = return_match.group(1).strip() if return_match else ''

    return ctx


def find_placeholders(content: str) -> List[str]:
    """Find all {{PLACEHOLDER}} patterns in content."""
    return re.findall(r'\{\{([A-Z_]+(?:_\d+)?)\}\}', content)


# ---------------------------------------------------------------------------
# HookEnricher
# ---------------------------------------------------------------------------

class HookEnricher:
    """Enriches hook markdown files with AI-generated content."""

    def __init__(self, workers: int = 5, rate_limit: float = 2.0):
        self.provider = os.getenv('AI_PROVIDER', 'gemini').lower()

        # Auto-adjust based on provider.
        if rate_limit == 2.0:
            provider_limits = {
                'claude': 1.0,
                'gemini': 20.0,
                'openai': 10.0,
                'openrouter': 5.0,
            }
            rate_limit = provider_limits.get(self.provider, 2.0)

        if workers == 5:
            provider_workers = {
                'claude': 3,
                'gemini': 10,
                'openai': 8,
                'openrouter': 5,
            }
            workers = provider_workers.get(self.provider, 5)

        self.workers = workers
        self.rate_limit = rate_limit
        self.request_interval = 1.0 / rate_limit
        self.last_request_time = 0
        self.lock = Lock()
        self.processed_count = 0
        self.total_count = 0
        self.failed_items = []

        print(f"Using AI provider: {self.provider}")
        print(f"Auto-adjusted: {self.workers} workers at {self.rate_limit} requests/second")

        if self.provider not in AI_PROVIDERS:
            print(f"Error: Unknown AI provider '{self.provider}'")
            sys.exit(1)

        if not AI_PROVIDERS[self.provider]:
            packages = {
                'gemini': 'google-genai',
                'claude': 'anthropic',
                'openai': 'openai',
                'openrouter': 'requests',
            }
            print(f"Error: {self.provider} package not installed.")
            print(f"Install with: pip install {packages.get(self.provider, 'unknown')}")
            sys.exit(1)

        self._initialize_provider()
        self._test_connection()

    # --- Provider initialization ---

    def _initialize_provider(self):
        if self.provider == 'gemini':
            api_key = os.getenv('GEMINI_API_KEY')
            if not api_key:
                print("Error: GEMINI_API_KEY not found in environment.")
                sys.exit(1)
            self.model = os.getenv('GEMINI_MODEL', 'gemini-2.5-flash-lite')
            self.client = genai.Client(api_key=api_key)

        elif self.provider == 'claude':
            api_key = os.getenv('ANTHROPIC_API_KEY')
            if not api_key:
                print("Error: ANTHROPIC_API_KEY not found in environment.")
                sys.exit(1)
            self.model = os.getenv('CLAUDE_MODEL', 'claude-sonnet-4-6-20250514')
            self.client = anthropic.Anthropic(api_key=api_key)

        elif self.provider == 'openai':
            api_key = os.getenv('OPENAI_API_KEY')
            if not api_key:
                print("Error: OPENAI_API_KEY not found in environment.")
                sys.exit(1)
            self.model = os.getenv('OPENAI_MODEL', 'gpt-4o-mini')
            self.client = openai.OpenAI(api_key=api_key)

        elif self.provider == 'openrouter':
            self.api_key = os.getenv('OPENROUTER_API_KEY')
            if not self.api_key:
                print("Error: OPENROUTER_API_KEY not found in environment.")
                sys.exit(1)
            self.model = os.getenv('OPENROUTER_MODEL', 'anthropic/claude-3.5-sonnet')
            self.base_url = "https://openrouter.ai/api/v1/chat/completions"

        print(f"Using model: {self.model}")

    def _test_connection(self):
        try:
            result = self._call_ai("Respond with just the word OK.")
            if 'OK' not in result:
                print(f"Warning: Unexpected test response: {result[:50]}")
        except Exception as e:
            print(f"Error testing {self.provider} connection: {e}")
            raise

    # --- AI call ---

    def _call_ai(self, prompt: str, max_tokens: int = 500, max_retries: int = 4) -> str:
        for attempt in range(max_retries):
            try:
                if self.provider == 'gemini':
                    response = self.client.models.generate_content(
                        model=self.model,
                        contents=prompt,
                    )
                    return (response.text or '').strip()

                elif self.provider == 'claude':
                    response = self.client.messages.create(
                        model=self.model,
                        max_tokens=max_tokens,
                        messages=[{"role": "user", "content": prompt}],
                    )
                    return (response.content[0].text or '').strip()

                elif self.provider == 'openai':
                    response = self.client.chat.completions.create(
                        model=self.model,
                        messages=[{"role": "user", "content": prompt}],
                        max_tokens=max_tokens,
                    )
                    return (response.choices[0].message.content or '').strip()

                elif self.provider == 'openrouter':
                    resp = requests.post(
                        self.base_url,
                        headers={
                            "Authorization": f"Bearer {self.api_key}",
                            "Content-Type": "application/json",
                        },
                        json={
                            "model": self.model,
                            "messages": [{"role": "user", "content": prompt}],
                            "max_tokens": max_tokens,
                        },
                    )
                    resp.raise_for_status()
                    return (resp.json()["choices"][0]["message"]["content"] or '').strip()

            except Exception as e:
                error_str = str(e)
                is_retryable = any(s in error_str.lower() for s in ['503', 'unavailable', 'overloaded', '429', 'rate'])
                if is_retryable and attempt < max_retries - 1:
                    backoff = 5 * (2 ** attempt)
                    print(f"  Retrying in {backoff}s (attempt {attempt + 1}/{max_retries})...")
                    time.sleep(backoff)
                    continue
                raise

        return ''

    def _rate_limit(self):
        with self.lock:
            now = time.time()
            elapsed = now - self.last_request_time
            if elapsed < self.request_interval:
                time.sleep(self.request_interval - elapsed)
            self.last_request_time = time.time()

    # --- Prompt builders ---

    def _build_prompt(self, placeholder: str, ctx: Dict[str, str]) -> str:
        hook_id = ctx['hook_id']
        hook_type = ctx['hook_type']
        params = ctx['parameters']
        source = ctx['source_code']
        existing_desc = ctx['existing_description']
        internal_usage = ctx['internal_usage']
        wp_func = 'add_filter' if hook_type == 'filter' else 'add_action'

        if placeholder == 'READABLE_DESCRIPTION':
            return f"""Write a 1-2 sentence description for this WordPress {hook_type} hook.

Hook: {hook_id}
Type: {hook_type}
Integration: {ctx['integration']}
Existing docblock description: {existing_desc}
Parameters:
{params}

The description should:
- Be 10-25 words
- Start with "Fires" (for actions) or "Filters" (for filters)
- Focus on what this hook does and when it fires
- Be clear and user-friendly

Generate only the description text, no formatting or quotes."""

        elif placeholder == 'TECHNICAL_DESCRIPTION':
            return f"""Write a detailed developer description (30-80 words) for this WordPress {hook_type} hook.

Hook: {hook_id}
Type: {hook_type}
Integration: {ctx['integration']}
Existing description: {existing_desc}
Parameters:
{params}
Source context:
{source[:500]}
Internal usage:
{internal_usage[:300]}

Include: when this hook fires in the lifecycle, what developers can do with it, and any important caveats.
Write only the description text, no formatting or markdown."""

        elif placeholder == 'USAGE_EXAMPLE':
            examples_ctx = ''
            for ex in ctx.get('existing_examples', []):
                if 'your_function_name' not in ex:
                    examples_ctx += f"\nExisting example:\n{ex}\n"

            return f"""Write a realistic PHP code example for this WordPress {hook_type} hook.

Hook: {hook_id}
Type: {hook_type}
Parameters:
{params}
Source context:
{source[:600]}
{examples_ctx}
Internal usage:
{internal_usage[:300]}

Requirements:
- Write a complete {wp_func}() example with real logic, not a stub
- Include proper parameter count in the priority/accepted args
- If it's a filter, show a return statement
- Use realistic variable names and logic a WordPress developer would actually write
- Include a brief comment explaining what the example does
- Do NOT wrap in markdown fences — output only the raw PHP code

Output only the PHP code."""

        elif placeholder.startswith('PARAM_DESCRIPTION_'):
            # Extract the param index.
            idx = placeholder.replace('PARAM_DESCRIPTION_', '')
            return f"""Describe this parameter for the WordPress hook {hook_id}.

Hook type: {hook_type}
All parameters:
{params}
Source context:
{source[:400]}

Write one sentence describing what parameter #{idx} contains or is used for.
Output only the description text."""

        elif placeholder == 'RETURN_DESCRIPTION':
            return f"""Describe the expected return value for this WordPress filter hook.

Hook: {hook_id}
Parameters:
{params}
Source context:
{source[:400]}
{ctx['return_section']}

Write one sentence describing what the filter callback should return.
Output only the description text."""

        return f"Describe the WordPress hook '{hook_id}' in one sentence."

    # --- File processing ---

    def _process_file(self, file_path: Path, force: bool = False) -> Tuple[bool, str, Optional[str]]:
        """Process a single markdown file. Returns (changed, filename, error)."""
        try:
            content = file_path.read_text(encoding='utf-8')
            placeholders = find_placeholders(content)

            if not placeholders and not force:
                return False, file_path.name, None

            ctx = extract_context_from_md(content)

            for ph in placeholders:
                self._rate_limit()
                prompt = self._build_prompt(ph, ctx)
                result = self._call_ai(prompt)

                if not result or len(result) < 5:
                    continue

                # Clean up: remove markdown fences if AI added them.
                if ph == 'USAGE_EXAMPLE':
                    result = re.sub(r'^```(?:php)?\n?', '', result)
                    result = re.sub(r'\n?```$', '', result)
                    # Wrap in fenced block for the markdown.
                    result = f"```php\n{result}\n```"

                content = content.replace(f'{{{{{ph}}}}}', result)

            file_path.write_text(content, encoding='utf-8')

            with self.lock:
                self.processed_count += 1
                if self.processed_count % 5 == 0:
                    pct = (self.processed_count / self.total_count) * 100
                    print(f"  Processing: {self.processed_count}/{self.total_count} ({pct:.1f}%) — {ctx['hook_id']}")

                if self.processed_count % 250 == 0:
                    print(f"  Pausing for 10 seconds after {self.processed_count} items...")
                    time.sleep(10)

            return True, file_path.name, None

        except Exception as e:
            with self.lock:
                self.failed_items.append({
                    'file': str(file_path),
                    'error': str(e),
                })
            return False, file_path.name, str(e)

    # --- Main entry point ---

    def enrich_all(self, input_dir: str, force: bool = False):
        """Enrich all markdown files in the input directory."""
        docs_path = Path(input_dir)
        if not docs_path.is_dir():
            print(f"Error: Input directory not found: {input_dir}")
            sys.exit(1)

        # Collect files that need processing.
        all_files = sorted(docs_path.rglob('*.md'))
        work_files = []

        for f in all_files:
            content = f.read_text(encoding='utf-8')
            if force or find_placeholders(content):
                work_files.append(f)

        self.total_count = len(work_files)
        print(f"Found {len(all_files)} total files, {self.total_count} need enrichment")

        if self.total_count == 0:
            print("All files already enriched — nothing to do.")
            return

        print(f"Using {self.workers} workers at {self.rate_limit} requests/second")

        start_time = time.time()
        changed_count = 0

        with ThreadPoolExecutor(max_workers=self.workers) as executor:
            futures = {
                executor.submit(self._process_file, f, force): f
                for f in work_files
            }

            for future in as_completed(futures):
                changed, filename, error = future.result()
                if changed:
                    changed_count += 1
                if error:
                    print(f"  FAILED: {filename} — {error[:80]}")

        duration = time.time() - start_time

        print(f"\nEnrichment complete!")
        print(f"  Duration: {duration:.1f}s")
        print(f"  Processed: {self.processed_count}/{self.total_count}")
        print(f"  Changed: {changed_count} files")
        print(f"  Failed: {len(self.failed_items)}")

        if self.failed_items:
            print(f"\nFailed items:")
            for item in self.failed_items[:10]:
                print(f"  - {item['file']}: {item['error'][:60]}")
            if len(self.failed_items) > 10:
                print(f"  ... and {len(self.failed_items) - 10} more")


