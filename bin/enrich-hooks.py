def main():
    parser = argparse.ArgumentParser(
        description='Enrich Automator hook documentation with AI-generated content.',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
    python3 enrich-hooks.py --input hook-docs/docs
    python3 enrich-hooks.py --input hook-docs/docs --force
    python3 enrich-hooks.py --input hook-docs/docs --workers 8 --rate-limit 10

Environment:
    AI_PROVIDER=gemini          (gemini|claude|openai|openrouter)
    GEMINI_API_KEY=...
    GEMINI_MODEL=gemini-2.5-flash-lite
    ANTHROPIC_API_KEY=...
    OPENAI_API_KEY=...
    OPENROUTER_API_KEY=...
        """,
    )

    parser.add_argument(
        '--input',
        required=True,
        help='Path to the docs directory containing .md files',
    )
    parser.add_argument(
        '--force',
        action='store_true',
        help='Re-generate all descriptions even if placeholders are already filled',
    )
    parser.add_argument(
        '--workers',
        type=int,
        default=5,
        help='Number of concurrent workers (default: auto per provider)',
    )
    parser.add_argument(
        '--rate-limit',
        type=float,
        default=2.0,
        help='Requests per second (default: auto per provider)',
    )

    args = parser.parse_args()

    enricher = HookEnricher(
        workers=args.workers,
        rate_limit=args.rate_limit,
    )

    enricher.enrich_all(
        input_dir=args.input,
        force=args.force,
    )


if __name__ == '__main__':
    main()
